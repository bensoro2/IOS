// ─── Helpers ──────────────────────────────────────────────────────────────────

export const getCountryByCode = (code: string): CountryData | undefined =>
  COUNTRIES.find((c) => c.code === code);

export const getSelectedCountryCode = (): string =>
  localStorage.getItem("selected_country") || "TH";

export const setSelectedCountryCode = (code: string) =>
  localStorage.setItem("selected_country", code);

/** Get localized province name. Works for all countries. */
export const getLocalizedProvinceName = (nativeName: string, language: string, countryCode?: string): string => {
  const code = countryCode || getSelectedCountryCode();
  const country = getCountryByCode(code);
  if (!country) return nativeName;

  // For Thai country + Thai language, just return native
  if (code === "TH" && language === "th") return nativeName;
  // For non-Thai countries where native = display language's script, return native
  if (code === "JP" && language === "ja") return nativeName;
  if (code === "KR" && language === "ko") return nativeName;
  if (code === "CN" && language === "zh") return nativeName;
  if (code === "RU" && language === "ru") return nativeName;
  if (code === "GB" && language === "en") return nativeName;

  const prov = country.provinces.find((pr) => pr.native === nativeName);
  return prov?.translations[language] || nativeName;
};

/** Get all searchable names for a province (native + all translations) */
export const getProvinceSearchString = (nativeName: string, countryCode?: string): string => {
  const code = countryCode || getSelectedCountryCode();
  const country = getCountryByCode(code);
  if (!country) return nativeName;
  const prov = country.provinces.find((pr) => pr.native === nativeName);
  if (!prov) return nativeName;
  const allNames = [nativeName, ...Object.values(prov.translations)];
  return allNames.join(" ");
};

/** Get province list for a country */
export const getProvincesForCountry = (countryCode: string): string[] => {
  const country = getCountryByCode(countryCode);
  return country ? country.provinces.map((pr) => pr.native) : [];
};

/** Get default province for a country */
export const getDefaultProvince = (countryCode: string): string => {
  const country = getCountryByCode(countryCode);
  return country?.defaultProvince || "";
};
